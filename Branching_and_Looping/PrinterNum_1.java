package Branching_and_Looping;

public class PrinterNum_1 {
    public static void main(String[] args) {
        //For loop
        System.out.println("Number 10 to 50 using for loop");
        for(int i =10; i <= 50; i++ )
        {
            System.out.println(i);
        }
        System.out.println();
         //while loop
        System.out.println("Number 10 to using while loop");
        int j = 10;
         while(j <= 50)
         {
             System.out.println(j);
             j++;
         }
    }
}
